package day3;

public class Increment {
 public static void main(String[] args) {
	 
	 int val1=10;
	 int val2=20;
	 
	 // incrementing both pre and post fix
	System.out.println("incrementing both pre and post fix");
	System.out.println("pre-incrementing val1 :  "+ ++val1);
	System.out.println("pre-incrementing val2 :  "+ ++val2);
	System.out.println("post-incrementing val1 :  "+ val1++);
	System.out.println("post-incrementing val2 :  "+ val2++);
}
}
